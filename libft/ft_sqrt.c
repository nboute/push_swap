int		ft_sqrt(int nb)
{
	int div;

	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	div = 2;
	while (div < nb / div)
		div++;
	if (div * div == nb)
		return (div);
	return (0);
}
